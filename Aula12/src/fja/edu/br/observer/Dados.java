package fja.edu.br.observer;


public class Dados {
    int valora;
    int valorb;
    int valorc;

    public Dados(int valora, int valorb, int valorc) {
        this.valora = valora;
        this.valorb = valorb;
        this.valorc = valorc;
    }

    public int getValora() {
        return valora;
    }

    public void setValora(int valora) {
        this.valora = valora;
    }

    public int getValorb() {
        return valorb;
    }

    public void setValorb(int valorb) {
        this.valorb = valorb;
    }

    public int getValorc() {
        return valorc;
    }

    public void setValorc(int valorc) {
        this.valorc = valorc;
    }
    
    
    
}
